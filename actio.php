    <div class="text-container">
        <p>
            The oceans provide 50 percent of the world’s oxygen and they provide essential ecosystem services that allow our planet to function in a healthy way. For example, ocean currents govern our weather systems, and if they were to be interrupted, the extreme weather consequences would be drastic. The oceans can only provide these services by maintaining delicately balanced ecosystems, made up of food chains and biological systems. Human activity risks the shutdown of essential ecosystem services that we rely on to survive.
            <br>
            <br>
            One example is coral reefs. When we fish around coral reefs, those reefs’ delicate ecosystems are damaged and often die. We then lose the essential service a coral reef provides such as protection from erosion and weather, biochemical regulation, and recreation. This is just one of the reasons why taking care of the oceans is essential for our own wellbeing.   
        </p>

        <h2 style="margin-bottom: 40px;"> MARINE LIFE POLLUTION </h2>
        <img src="images/action_pollution_img.webp">
        <p>
            <br>
            Humans continue to pollute the oceans, despite the huge risks to marine life and to ourselves. Marine pollution comes in many forms such as plastic pollution, acidification and oil spills.
        </p>
        
    <h3> How Does Ocean Acidification Affect Marine Life?</h3>
        <p>
            Ocean acidification occurs due to increases in carbon dioxide in the atmosphere (due to climate warming) and it can have terrible consequences for marine life, particularly crustaceans. Higher acidity in water results in shells dissolving more quickly, which of course has a devastating impact on shellfish and other crustaceans. This can have drastic knock-on consequences for other animals who rely on them for food, and throw marine food chains and whole ecosystems out of balance.
        </p>

    <h3>How Do Fisheries And Seafood Affect Marine Life?</h3>
        <p>
        Probably the largest risk posed to marine life is the fishing industry. Vast commercial fleets trawling the oceans has been common practice for many years and sadly shows no signs of slowing. The negative impacts for marine life are huge and includes:
        </p>
        <ul>
            <li>Fish population crashes which impacts every species that live in the oceans. Bycatch from fishing nets results in huge biodiversity loss, including driving species to the brink of extinction.</li>
            <br>
            <li>There is evidence to show that catching fish causes pain and is hugely distressing for those caught and those left behind.</li>
        </ul>

    <h3>How Does Fish Farming Affect Marine Life?</h3>
        <ul>
            <li>Disease is easily spread in fish farms and too often spreads beyond the farm into the natural environment.</li>
            <br>
            <li>Fish farms pollute waters with the chemicals needed to treat diseases caused by the appalling conditions, excess feed and concentrated fish waste. This pollution causes algal blooms, resulting in the deaths of wild fish, too.</li>
            <br>
            <li>Wild marine animals, such as seals, are inevitably attracted to fish farms as a potential source of food, but these animals are often killed deliberately by farmers to stop them eating the valuable fish.</li>
        </ul>
    
    <h3>How Does Human-generated Noise Pollution Affect Marine Life?</h3>
        <p>Natural soundscapes in the ocean are key for healthy marine life, as animals use sound to communicate, catch food, navigate, defend themselves and attract mates. Machinery like boats, jet skis or oil drills create an unnatural barrier to marine life’s natural soundscape, and the impacts can devastating:</p>
        <ul>
            <li>Marine mammals like whales use complex songs to communicate, navigate and attract one another. If these are interrupted, they cannot enact basic natural functions and die as a consequence</li>
            <br>
            <li>Animals can be directly stressed by unnatural noise, leading them to make poor decisions that can lead to their deaths</li>
        </ul>
    
    <h3>Other Forms Of Pollution?</h3>
        <p>Plastic pollution is the other major form of marine pollution that affects marine life:</p>
        <ul>
            <li>Plastic waste can entrap marine animals resulting in them drowning, starving or being eaten</li>
            <br>
            <li>Soft plastics and microplastics can be ingested by marine life and cause their deaths</li>
            <br>
            <li>Plastic pollution kills 100 million marine mammals and up to a million seabirds every year</li>
            <br>
            <li>Most plastic pollution comes from the fishing industry</li>
        </ul>
        <img src="images/action_pollution_img2.jpg" class="pollution">
    
    <h2 style="margin-bottom: 40px;">HOW CAN WE SAVE MARINE LIFE?</h2>
    <img src="images/action_marinellife_awareness.png" style="margin-bottom: 20px;">

        <p>
            Put simply, we can save marine life by leaving the oceans alone and letting their ecosystems operate naturally. These systems have developed over millions of years of evolution and they can only operate without our intrusion.
            <br><br>
            We can reduce our impact on marine life in a few ways:
        </p>
        <ul>
            <li>Reduce our consumption of all unnecessary products and buy local as much as we can – global shipping networks create noise and chemical pollution in the oceans, which directly affects marine life</li>
            <br>
            <li>Reduce our reliance on plastics, stop buying single-use plastic items, and recycle plastic waste effectively, so that it never ends up in the ocean</li>
            <br>
            <li>Raise awareness of the issues – watch and share documentaries such as Seaspiracy to alert others of the need to save marine life</li>
        </ul>
